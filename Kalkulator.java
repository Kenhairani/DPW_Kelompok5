    private void buttonHasiljButton29ActionPerformed(java.awt.event.ActionEvent evt) {
        switch (pilih) {
            case 1: // tambah
                bilangan2 = Double.parseDouble(angka);
                jumlah = bilangan1 + bilangan2;
                angka = formatHasil(jumlah);
                textFieldHasil.setText(angka);
                textFieldHasilHistory.setText(historyAngka);
                break;
            case 2: // kurang
                bilangan2 = Double.parseDouble(angka);
                jumlah = bilangan1 - bilangan2;
                angka = formatHasil(jumlah);
                textFieldHasil.setText(angka);
                textFieldHasilHistory.setText(historyAngka);
                break;
            case 3: // kali
                bilangan2 = Double.parseDouble(angka);
                jumlah = bilangan1 * bilangan2;
                angka = formatHasil(jumlah);
                textFieldHasil.setText(angka);
                textFieldHasilHistory.setText(historyAngka);
                break;
            case 4: // bagi
                bilangan2 = Double.parseDouble(angka);
                jumlah = bilangan1 / bilangan2;
                angka = formatHasil(jumlah);
                textFieldHasil.setText(angka);
                textFieldHasilHistory.setText(historyAngka);
                break;
            case 5: // persen
                if (bilangan1 != 0.0 & bilangan2 != 0.0) {
                    jumlah = ((bilangan1 * bilangan2) / 100);
                    angka = formatHasil(jumlah);
                    textFieldHasil.setText(angka);
                    textFieldHasilHistory.setText(historyAngka);
                } else {
                    jumlah = (bilangan2 / 100);
                    angka = formatHasil(jumlah);
                    textFieldHasil.setText(angka);
                    textFieldHasilHistory.setText(historyAngka);
                }
                break;
            default:
                break;
        }
    }

