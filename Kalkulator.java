    private void buttonMinusPositifbuttonMinusPositifActionPerformed(java.awt.event.ActionEvent evt) {
        if (!angka.isEmpty() && !angka.equals("0")) {
            if (angka.startsWith("-")) {
                angka = angka.substring(1);

                if (historyAngka.endsWith(angka)) {
                    historyAngka = historyAngka.substring(0, historyAngka.length() - angka.length() - 1);
                    historyAngka += angka;
                }
            } else {
                String angkaLama = angka;
                angka = "-" + angka;

                if (historyAngka.endsWith(angkaLama)) {
                    historyAngka = historyAngka.substring(0, historyAngka.length() - angkaLama.length());
                    historyAngka += angka;
                }
            }

            textFieldHasil.setText(angka);
            textFieldHasilHistory.setText(historyAngka);
        }
    }

